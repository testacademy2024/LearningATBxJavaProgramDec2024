package ex_22122024;

public class Lab015 {
    public static void main(String[] args) {

        // data_type variable_name = variable_value(literal)

        // Data Type
        // Primitive Data Type
        //  boolean
        // true or false
        boolean is_married=true;
        boolean are_you_dog=true;
        System.out.println("is_married");
        System.out.println("are_you_a_dog");
        // Numerical
        // integral - byte, short, int, long
        byte b=101;
        short s=101;
        int i=130;
        long phone_number=8097442458L;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(phone_number);
        //  char - char  - A,B
        char grade='A';
        System.out.println(grade);
        // float   this is to be used if only two numbers after decimal
        double pi=3.142;
        System.out.println(pi);
        //double  this is to be used when more tha two numbers are present after decimal
        float v =118.654f;
        System.out.println(v);
        // Non-primitive Data Types
        // String  name -> "sanket" -, number or char or bunch of char
        String name1 = "Sanket";
        String name2 = "U";
        System.out.println(name1+" "+name2);
        char cc = 'C';
    }
}
